def bucket_number(n, buckets):
    try:
        n = int(n or 0)
    except Exception:
        return "unknown"
    for upper, label in buckets:
        if n <= upper:
            return label
    return buckets[-1][1]


def _fmt_percent(value, decimals=2):
    try:
        value = float(value)
    except Exception:
        return "unknown"
    s = f"{value:.{decimals}f}".rstrip("0").rstrip(".")
    return f"{s}%"


def bucket_percent(p):
    """Fine-grained 5 percentage-point buckets for ratios/rates.

    0% is isolated from any non-zero presence.
    """
    if p is None:
        return "unknown"
    try:
        p = float(p)
    except Exception:
        return "unknown"
    if p <= 0:
        return "0%"
    if p > 100:
        p = 100
    upper = 5
    while upper < 100:
        if p <= upper:
            lower = upper - 5
            if lower == 0:
                return ">0–5%"
            return f"{lower}–{upper}%"
        upper += 5
    return "95–100%"


def exact_percent(p, decimals=2):
    if p is None:
        return "unknown"
    return _fmt_percent(p, decimals=decimals)


def bucket_retention(p):
    """Retention stays exact in the payload.

    For analysis, point-level retention is useful. The usage_fingerprint builder
    later normalizes this exact value into a stable bucket so tiny changes do
    not create fake unique profiles.
    """
    if p is None:
        return "unknown"
    try:
        p = float(p)
    except Exception:
        return "unknown"
    if p < 0:
        p = 0
    if p > 100:
        p = 100
    return exact_percent(p, decimals=2)


